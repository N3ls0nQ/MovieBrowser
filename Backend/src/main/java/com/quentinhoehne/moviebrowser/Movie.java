package com.quentinhoehne.moviebrowser;

public class Movie {
    public String name;
    public int year;
    public String image;
    public int id;

    public Movie(String name, int year, String image, int id) {
        this.name = name;
        this.year = year;
        this.image = image;
        this.id = id;
    }
}
